# 2 - Exercício: Estruturas Condicionais (IA Expert Academy)

num_1 = float(input("Digite um número: "))
num_2 = float(input("Digite outro número: "))

if num_1 > num_2:
    print("O primeiro número é maior!.")
elif num_1 < num_2:
    print("O segundo número é maior!.")
else:
    print(f"Os dois números {num_1:.2f} e {num_2:.2f} são iguais!.")
