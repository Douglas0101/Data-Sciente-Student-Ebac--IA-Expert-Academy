a = float(input('Digite o primeiro número: '))
b = float(input('Digite o segundo número: '))

ad = a + b
sub = a - b
mult = a * b
div = a / b
pot = a ** b

print(f'{a} + {b} = {ad:.2f}')
print(f'{a} - {b} = {sub:.2f}')
print(f'{a} * {b} = {mult:.2f}')
print(f'{a} / {b} = {div:.2f}')
print(f'{a} ** {b} = {pot:.2f}')